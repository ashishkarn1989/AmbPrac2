					<td class="italic key">Mission:</td>
					<td>
						<ol>
							<li>Serve as a committee of experts in ambulatory clinical practice that makes decisions regarding the ever increasing regulatory, scope of practice and compliance demands.</li>
							<li>Serve as a central forum to define and communicate information related to ambulatory clinical practice policies and evidence based practices in all clinical disciplines.</li>
							<li>Align work with the strategies of the organization and provide standardization around ambulatory clinical practice.</li>
							<li>Collaborate with clinical leaders across the continuum to ensure true integration of the members’ care experience.</li>
							<li>Incorporate the American Academy of Ambulatory Care Nursing (AAACN) <span class="underline">Standards of Practice for Professional Ambulatory Care Nursing</span> into nursing practice.</li>
							<li>Incorporate the Joint Commission Ambulatory Care National Patient Safety Goals into clinical practice.</li>
							<li>Embed the Institute of Medicine’s Future of Nursing recommendations in ambulatory practice as appropriate.</li>
							<li>Develop strategies to improve the quality of care and patient safety in an environment of increasing acuity and increasing volume of ambulatory procedures.</li>
							<li>Develop annual initiatives, metrics and outcomes in alignment with the Medical Group Administrators’ areas of focus (access, quality and safety, affordability, people and systems and the care experience.)</li>
						</ol>
